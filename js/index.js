    let gameInfoIds = gameIds.gameInfoIds;
    for (let DOMelement in gameInfoIds) {
        if (gameInfoIds.hasOwnProperty(DOMelement)) {
            resetTextById(gameInfoIds[DOMelement]);
        }
    }
